# Basic mathematics in Python Tutorial
# Reference - https://www.geeksforgeeks.org/basic-operators-python/

print('\"Arithmetic Operators\"')

a = 15
b = 4

# Output: a + b = 19
print('a + b =',a+b)

# Output: a - b = 11
print('a - b =',a-b)

# Output: a * b = 60
print('a * b =',a*b)

# Output: a / b = 3.75
print('a / b =',a/b)

# Output: a // b = 3
print('a // b =',a//b)

# Output: a ** b = 50625
print('a ** b =',a**b)


print('\n\"Comparison operators\"')
x = 10
y = 12

# Output: x > y is False
print('x > y is',x>y)

# Output: x < y is True
print('x < y is',x<y)

# Output: x == y is False
print('x == y is',x==y)

# Output: x != y is True
print('x != y is',x!=y)

# Output: x >= y is False
print('x >= y is',x>=y)

# Output: x <= y is True
print('x <= y is',x<=y)

print('\n\"Logical Operators\"')
m = True
n = False

# Output: m and n is False
print('m and n is',m and n)

# Output: m or n is True
print('m or n is',m or n)

# Output: not m is False
print('not m is',not m)



